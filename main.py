def calculate_bmr(gender, weight_kg, height_cm, age):
    if gender.lower() == 'male':
        bmr = 10 * weight_kg + 6.25 * height_cm - 5 * age + 5
    elif gender.lower() == 'female':
        bmr = 10 * weight_kg + 6.25 * height_cm - 5 * age - 161
    else:
        raise ValueError("Gender must be 'male' or 'female'")
    return round(bmr)

def calculate_daily_burn(bmr, activity_level):
    activity_factors = {
        "sedentary": 1.2,
        "light": 1.375,
        "moderate": 1.55,
        "active": 1.725,
        "very active": 1.9
    }
    return round(bmr * activity_factors.get(activity_level, 1))

def adjust_calories_for_goal(daily_burn):
    global amount
    goal = input("Are you trying to lose, gain, or maintain your weight? ").lower()
    if goal == "lose":
        amount = float(input("How many kilograms are you trying to lose? "))
        # Assuming a goal of 1kg per week weight loss, which requires about a 1000 calorie daily deficit
        adjusted_burn = daily_burn - 1000
    elif goal == "gain":
        amount = float(input("How many kilograms are you trying to gain? "))
        # Assuming a moderate weight gain goal, which requires about a 250 calorie daily surplus
        adjusted_burn = daily_burn + 250
    else:  # maintain
        adjusted_burn = daily_burn
    return adjusted_burn, goal, amount

